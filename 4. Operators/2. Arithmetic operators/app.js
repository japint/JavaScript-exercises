//Increment/Decrement operations
console.log('Increment/Decrement Operations');

var x = 0, y = 5, z = 0;
console.log('Postfix increment');
console.log('First output: ' + x++);
console.log('Second output: ' + x++);
console.log('Third output: ' + x);

console.log('Postfix decrement');
console.log('First output: ' + y--);
console.log('Second output: ' + y--);
console.log('Third output: ' + y);

x = 0, y = 5, z = 0;

console.log('Postfix increment');
console.log('First output: ' + ++x);
console.log('Second output: ' + ++x);
console.log('Third output: ' + x);

console.log('Postfix decrement');
console.log('First output: ' + --y);
console.log('Second output: ' + --y);
console.log('Third output: ' + y);
